#!/bin/bash

cowsay -f elephant Installing LibRetroCore RetroArch binaries archive...
echo Installing LibRetroCore RetroArch binaries archive...
echo Requires 14MB for archive plus 80MB total for binaries, some will be overwritten...

pushd /opt/retropie
sudo rm libretrocores.7z
sudo megadl 'https://mega.co.nz/#!4IlUEL7b!glvIQ5OSJZFrC6kul51XOBn8i8Oz_-uQECEAYYuiwYY'
echo Extracting 7z archive and installing.....
sudo 7z -d libretrocores.7z
rm libretrocores.7z
popd

echo Ensuring required rom folders exist...
mkdir /home/pi/RetroPie/roms/3do

