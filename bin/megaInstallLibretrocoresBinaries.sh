#!/bin/bash

cowsay -f elephant Installing LibRetroCore RetroArch binaries archive...
echo =====================> Installing LibRetroCore RetroArch binaries archive...
echo Requires 12MB for archive plus 64MB total for binaries, some will be overwritten...

pushd /opt/retropie
sudo rm libretrocores.7z
sudo megadl 'https://mega.co.nz/#!pNMgGb6T!JFDqnFzg7IsaIhZLsnEb4hGq4u6qRrNZhfTVa-mmFVs'
echo Extracting 7z archive and installing.....
sudo 7z -d libretrocores.7z
rm libretrocores.7z
popd

echo Ensuring required rom folders exist...
mkdir /home/pi/RetroPie/roms/3do

echo If you saw any errors, you might consider running a quickUpdatePrimestationOneFiles.sh to ensure you have the latest mega dl link in this script...
